package model;

/**
 * Application object
 */
public class Application {
    String id;
    Integer user;
    String content;
    String name;

    public void setId(String id) {
        this.id = id;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public void setName(String name) {
        this.name = name;
    }
}
